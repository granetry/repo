import math

# Incriments, both lines of code will alter x the same
x = 10
x = x + 3
x += 3  # Augmented assignment operator
x = x - 3
x - + 3
print(x)

# DIVISION
print(10/3)
print(10 // 3)  # will give integer
print(10 % 3)  # Outputs remainder of 10 / 3

# ROUNDING / ABS VALUE
x = -3.55
round(x)
abs(x)

# MATH module
math.floor(2.9)
math.ceil(2.9)
pi = math.pi
math.sin(pi / 2)
